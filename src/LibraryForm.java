    private DefaultTableModel setBooksTable(){
    	 int columnCount = 4;
         Vector data = new Vector(columnCount);
         Vector row = new Vector(columnCount);
         Vector columnNames = new Vector(columnCount);

        columnNames.addElement("id");
        columnNames.addElement("nazwa");
        columnNames.addElement("autor/numer");
        columnNames.addElement("dostępność");
        int i = 0;
        while (i < library.getVolumes().size()) {
             row.addElement(library.getVolumes().get(i).getUniqueId());
             row.addElement(library.getVolumes().get(i).getName());
             
             if(library.getVolumes().get(i).getClass().equals(Book.class)) {
             	row.addElement(library.getVolumes().get(i).getAuthor());	
             }else {row.addElement(library.getVolumes().get(i).getNumber());}
             
             row.addElement(library.getVolumes().get(i).checkAvailability());
             i++;
             data.addElement(row);
             row = new Vector(columnCount); // Create a new row Vector
         }
         DefaultTableModel model = new DefaultTableModel(data, columnNames);  
         return model;
    }
    
