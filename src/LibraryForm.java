    private void addBookButtonMouseClicked(java.awt.event.MouseEvent evt) {   
    	try{
    	library.createVolume(new Book(bookAuthorField.getText(), bookTitleField.getText()));
    	}catch(Exception e) {
    		consoleBooks.setText("Błąd");
    	}
    	consoleBooks.setText("Dodano książkę!");
    	bookAuthorField.setText("");
    	bookTitleField.setText("");
    	booksList.setModel(setBooksTable());
    	library.save();
    }                                          

    private void addLoanActionPerformed(java.awt.event.ActionEvent evt) {  
    	 Volume volume = library.findVolume(loanBookField.getText());
         Reader reader = library.findReader(loanSurnameField.getText());
         if(volume == null || reader == null){
        	 consoleLoans.setText("Błędne dane.");
        	 return;
         }
         if(library.addVolumeToReader(volume, reader) == true) {
        	 consoleLoans.setText("Wypożyczono książkę!");
        	 loanBookField.setText("");
        	 loanSurnameField.setText("");
        	 booksList.setModel(setBooksTable());
        	 jTable1.setModel(setReadersTable());
        	 library.save();
         }
         else consoleLoans.setText("Błąd! Czytelnik ma zbyt dużo książek.");
    }                                       

    private void removeLoanButtonActionPerformed(java.awt.event.ActionEvent evt) { 
    	Volume volume = library.findVolume(removeLoanIdField.getText());
    	if(volume == null){
       	 consoleLoans.setText("Błędne dane.");
       	 return;
        }
    	if(library.removeVolumeFromOwner(volume)== true){
    		consoleLoans.setText("Usunięto wypożyczenie");
    		booksList.setModel(setBooksTable());
    		jTable1.setModel(setReadersTable());
    		library.save();
    	}else consoleLoans.setText("Błąd!");
    }                                                

